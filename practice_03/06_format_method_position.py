# Format method with positioning
print("The result of the test is a(n) {result:<9} for Carlos.".format(
    result='"A"'))
print("The result of the test is a(n) {result:>9} for Peter.".format(
    result='"C"'))
print("The result of the test is a(n) {result:^9} for Putty.".format(
    result='"B'))
